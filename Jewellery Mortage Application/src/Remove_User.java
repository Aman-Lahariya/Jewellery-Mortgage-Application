    private void bt_RemoveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_RemoveUserActionPerformed
        // TODO add your handling code here:
        String Username;
        Username = cb_UserName.getSelectedItem().toString();
        if(Username == "Select")
        {
            JOptionPane.showMessageDialog(this,"Please select user name...");
        }
        else
        {
          int cnf = JOptionPane.showConfirmDialog(null,"Do you really want to revome user "+ Username +" !","Confirm",JOptionPane.YES_NO_OPTION);
          if(cnf == 0)
          {
            try
            {
                int cnt = 2;
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                if(Usertype.equals("Admin"))
                {
                    String query1 = "Select count(*) from users where utype = '"+Usertype+"'";
                    ResultSet rs = stmt.executeQuery(query1);
                    while(rs.next())
                    {
                        cnt = rs.getInt(1);
                    }  
                }
                
                if(cnt <=1)
                {
                    JOptionPane.showMessageDialog(this,"You can not delete the last remaining Admin!");
                }
                else
                {
                    String query2 = "Delete from users where uname = '"+Username+"'";
                    int r = stmt.executeUpdate(query2);
                    JOptionPane.showMessageDialog(this,"User " +Username+" removed...");
                }
                
                filldetails();
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
          }
        }
    }//GEN-LAST:event_bt_RemoveUserActionPerformed

