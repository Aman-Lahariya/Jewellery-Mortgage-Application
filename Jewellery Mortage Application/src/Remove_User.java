     // Function to fill combo box user_type & user_name.
     void filldetails()
     {
        //String Usertype;
        Usertype = cb_UserType.getSelectedItem().toString();
        if(Usertype == "Select")
        {
            JOptionPane.showMessageDialog(this,"Please select valid user type...");
           // cb_UserType.requestFocus();
            cb_UserName.removeAllItems();
            cb_UserName.addItem("Select");
        }
        else
        {
            try
            {
                cb_UserName.removeAllItems();
                cb_UserName.addItem("Select");
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select uname from users where utype = '"+Usertype+"'";
                ResultSet rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    cb_UserName.addItem(rs.getString("uname"));
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
        }   
    }

