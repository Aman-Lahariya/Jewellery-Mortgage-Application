    private void lbl_AddCityMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_AddCityMouseClicked
        // TODO add your handling code here:
        String cname = "";
        cname = JOptionPane.showInputDialog("Enter City Name");
        if(!cname.equals(""))
        {
            try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Insert into city values('"+ cname +"')";
                int r = stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"City added!");
                fillCity();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_lbl_AddCityMouseClicked

