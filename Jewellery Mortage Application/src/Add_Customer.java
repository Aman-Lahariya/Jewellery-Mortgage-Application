    private void bt_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_AddActionPerformed
        // TODO add your handling code here:
        
        // Validation         
        if(txt_Fname.getText().equals("") || txt_Lname.getText().equals("") || txt_FatherName.getText().equals("") || cb_city.getSelectedItem().equals("Select") || txt_Address.getText().equals("") )
        {
            JOptionPane.showMessageDialog(this,"Please select/enter data in all fields !");
        }
        else
        {
            try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                MyFunctions my = new MyFunctions();
                int id = my.cid();
                String fullname = txt_Fname.getText()+" "+txt_Lname.getText();
                String query = "Insert into customer values("+id+",'"+ txt_Fname.getText() +"','"+ txt_Lname.getText() +"','"+ txt_FatherName.getText() +"','"+ txt_Address.getText() +"','"+ cb_city.getSelectedItem() +"','"+ txt_Phno.getText() +"','"+fullname+"')";
                int r = stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Customer details added !");
                //int cnf = JOptionPane.showConfirmDialog(this,"Do you want to clear all the fields ?","Confirm",JOptionPane.YES_NO_OPTION);
               // if(cnf == 0)
                //{
                  clearAll();  
               // }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_bt_AddActionPerformed

