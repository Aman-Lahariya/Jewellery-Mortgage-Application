    void fillCity()
    {
        cb_city.removeAllItems();
        cb_city.addItem("Select");
        try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select * from city order by cname";
                ResultSet rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    cb_city.addItem(rs.getString("cname"));
                }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
    }
    
