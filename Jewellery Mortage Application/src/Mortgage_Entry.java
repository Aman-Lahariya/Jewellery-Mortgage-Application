    private void cmd_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_AddActionPerformed
        // TODO add your handling code here:
        if(txt_Receipt.getText().equals("") || txt_Weight.getText().equals("") || txt_Purity.getText().equals("") || cb_customer.getSelectedItem().equals("Select") || txt_OtherInfo.getText().equals("") || cb_Jewellery.getSelectedItem().equals("Select") || cb_ornament.getSelectedItem().equals("Select") || txt_Amt_Given.getText().equals("") || txt_InterestRate.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please select/enter data in all fields !");
        }
        else
        {
            try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                int mid = mf.mid();
                int cid = mf.retreive_cid(cb_customer.getSelectedItem().toString());
                String entryDate;
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
                entryDate = sdf.format(jDateChooser1.getDate());
                String query = "Insert into mortgage_entry values("+mid+","+cid+",'"+ txt_Receipt.getText() +"','"+ entryDate +"','"+ cb_Jewellery.getSelectedItem() +"','"+ cb_ornament.getSelectedItem() +"','"+ txt_Weight.getText() +"','"+ txt_Purity.getText() +"','"+ txt_OtherInfo.getText() +"','"+ txt_Amt_Given.getText() +"','"+ txt_InterestRate.getText() +"','Yet to Return')";
                int r = stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Details added !");
                int cnf = JOptionPane.showConfirmDialog(this,"Do you want to clear all the fields ?","Confirm",JOptionPane.YES_NO_OPTION);
                if(cnf == 0)
                {
                  clearAll();  
                }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_cmd_AddActionPerformed

