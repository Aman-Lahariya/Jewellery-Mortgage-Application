    private void cb_customerFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cb_customerFocusLost
        // TODO add your handling code here:
         String name = cb_customer.getSelectedItem().toString();
        if(name == "Select")
        {
            txt_FatherName.setText("");
            JOptionPane.showMessageDialog(this,"Please select customer name!");
        }
        else
        {
            MyFunctions mf = new MyFunctions();
            int cid = mf.retreive_cid(name);
            try
            {
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select fathername from customer where cid = "+cid+" ";
                ResultSet rs1 = stmt.executeQuery(query);
                rs1.next();
                txt_FatherName.setText(rs1.getString("fathername"));
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_cb_customerFocusLost

