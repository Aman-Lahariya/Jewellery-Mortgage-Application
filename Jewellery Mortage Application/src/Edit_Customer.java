    void fillCustomer()
    {
        cb_Name.removeAllItems();
        cb_Name.addItem("Select");
        //String cname;
        try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select fullname from customer order by fname";
                ResultSet rs = stmt.executeQuery(query);
                while(rs.next())
                {   
                    cb_Name.addItem(rs.getString("fullname"));
                }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
    }
    
    void fillCity()
    {
        cb_city.removeAllItems();
        cb_city.addItem("Select");
        try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select * from city order by cname";
                ResultSet rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    cb_city.addItem(rs.getString("cname"));
                }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
    }

