    private void bt_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SaveActionPerformed
        // TODO add your handling code here:

        // Validation
        if(txt_Fname.getText().equals("") || txt_Lname.getText().equals("") || txt_FatherName.getText().equals("") || cb_city.getSelectedItem().equals("Select") || txt_Address.getText().equals("") || txt_Phno.getText().equals("") )
        {
            JOptionPane.showMessageDialog(this,"Please select/enter data in all fields !");
        }
        else
        {
            try
            {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                int id = mf.cid();
                String fullname = txt_Fname.getText()+" "+txt_Lname.getText();
                String query = "update customer set fname = '"+ txt_Fname.getText() +"',lname = '"+ txt_Lname.getText() +"',fathername = '"+ txt_FatherName.getText() +"',address = '"+ txt_Address.getText() +"',city = '"+ cb_city.getSelectedItem() +"',phoneno = '"+ txt_Phno.getText() +"',fullname = '"+fullname+"' where cid = "+cid+"";
                int r = stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Customer details updated !");
                clearAll();
                fillCustomer();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_bt_SaveActionPerformed

