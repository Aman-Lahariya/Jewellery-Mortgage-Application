    private void cb_NameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cb_NameFocusLost
        // TODO add your handling code here:
        String cname = "";
        cname = cb_Name.getSelectedItem().toString();
        if(cname == "Select" || cname == "")
        {
            txt_Fname.setText("");
            txt_Lname.setText("");
            txt_FatherName.setText("");
            txt_Address.setText("");
            cb_city.setSelectedIndex(0);
            txt_Phno.setText("");
        }
        else
        {
            MyFunctions mf = new MyFunctions();
            cid = mf.retreive_cid(cname);
            try
            {
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                String query = "Select * from customer where cid = "+cid+"";
                ResultSet rs = stmt.executeQuery(query);
                rs.next();
                   
                    txt_Fname.setText(rs.getString("fname"));
                    txt_Lname.setText(rs.getString("lname"));
                    txt_FatherName.setText(rs.getString("fathername"));
                    txt_Address.setText(rs.getString("address"));
                    cb_city.setSelectedItem(rs.getString("city"));
                    txt_Phno.setText(rs.getString("phoneno"));
                 
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_cb_NameFocusLost

