        // TODO add your handling code here:
        String pass,cpass;
        pass = new String(txt_Password.getPassword());
        cpass = new String(txt_CnfrmPass.getPassword());
        
        if(pass.equals("") || cb_UserType.getSelectedItem().equals("Select") || txt_UserName.getText().equals("") )
        {
            JOptionPane.showMessageDialog(this,"Please enter data in all the fields !"); 
        }
        else
        {
            if(cpass.equals(pass))
            {
             try
             {
                MyFunctions mf = new MyFunctions();
                Connection cn = mf.myConnection();
                Statement stmt = cn.createStatement();
                MyFunctions my = new MyFunctions();
                int id = my.userid();
                String query2 = "Insert into users values("+id+",'"+txt_UserName.getText()+"','"+txt_Password.getText()+"','"+cb_UserType.getSelectedItem()+"')";
                int r = stmt.executeUpdate(query2);
                JOptionPane.showMessageDialog(this,"User details inserted!");
                cb_UserType.setSelectedIndex(0);
                txt_UserName.setText("");
                txt_Password.setText("");
                txt_CnfrmPass.setText("");
             }
             catch(Exception e)
             {
                JOptionPane.showMessageDialog(this, e);
             }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Please enter same password in both fields!");
                txt_Password.setText("");
                txt_CnfrmPass.setText("");
            }
        }
        
