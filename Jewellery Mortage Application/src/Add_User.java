    private void txt_UserNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_UserNameFocusLost
        // TODO add your handling code here:
        String UserName = txt_UserName.getText();
        if(UserName.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please enter username ...");
            txt_UserName.grabFocus();
        }
        else
        {
         try
         {
            MyFunctions mf = new MyFunctions();
            Connection cn = mf.myConnection();
            Statement stmt = cn.createStatement();
            String query1 = "Select * from users where uname = '"+txt_UserName.getText()+"'";
            ResultSet rs = stmt.executeQuery(query1);
            if(rs.next())
            {
             JOptionPane.showMessageDialog(this,"User already exits....\nPlease select different username!");   
             txt_UserName.grabFocus();
            }
         }
         catch(Exception e)
         {
            JOptionPane.showMessageDialog(this, e);
         }
        }
    }//GEN-LAST:event_txt_UserNameFocusLost

